package com.youthministry.controller.layout.impl;

public class LayoutHelper {
	
	private String layoutName;
	
	private String layoutDescription;
	
	private String jsonLayout;

	public String getLayoutName() {
		return layoutName;
	}

	public void setLayoutName(String layoutName) {
		this.layoutName = layoutName;
	}

	public String getLayoutDescription() {
		return layoutDescription;
	}

	public void setLayoutDescription(String layoutDescription) {
		this.layoutDescription = layoutDescription;
	}

	public String getJsonLayout() {
		return jsonLayout;
	}

	public void setJsonLayout(String jsonLayout) {
		this.jsonLayout = jsonLayout;
	}


}
